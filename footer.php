<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Select all the elements with .ffcc-head class
        const headers = document.querySelectorAll('.ffcc-head');

        // Function to handle the toggle behavior
        headers.forEach(function(header) {
            header.addEventListener('click', function() {
                const container = header.nextElementSibling; // The ffcc-container element
                const addIcon = header.querySelector('.add-icon');
                const subIcon = header.querySelector('.sub-icon');

                // Only apply this behavior when the screen width is 680px or less
                if (window.innerWidth <= 680) {
                    if (container.style.display === 'none' || container.style.display === '') {
                        container.style.display = 'block'; // Show the ffcc-container
                        addIcon.style.display = 'none'; // Hide the add-icon
                        subIcon.style.display = 'inline'; // Show the sub-icon
                    } else {
                        container.style.display = 'none'; // Hide the ffcc-container
                        addIcon.style.display = 'inline'; // Show the add-icon
                        subIcon.style.display = 'none'; // Hide the sub-icon
                    }
                } else {
                    addIcon.style.display = 'none'; // Show the add-icon
                    subIcon.style.display = 'none';
                }
            });
        });

        // Ensure all ffcc-container are hidden initially for mobile screens
        function hideContainersOnMobile() {
            if (window.innerWidth <= 680) {
                document.querySelectorAll('.ffcc-container').forEach(function(container) {
                    container.style.display = 'none';
                });
            } else {
                document.querySelectorAll('.ffcc-container').forEach(function(container) {
                    container.style.display = 'flex';
                });
            }
        }

        // Call function on load and on window resize
        hideContainersOnMobile();
        window.addEventListener('resize', hideContainersOnMobile);
    });
</script>